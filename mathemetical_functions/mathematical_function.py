import math
res = math.fabs(-10) #absolute function
print(res)

res = math.sqrt(16) #Square root function
print(res)

res = math.pow(10,2) #power function
print(res)

res = math.fmod(10,3) #mod function
print(res)

res = math.floor(10.9) #floor function
print(res)

res = math.ceil(10.1) #ceil function
print(res)

res = math.trunc(10.9)
print(res)

res = math.factorial(5)
print(res)